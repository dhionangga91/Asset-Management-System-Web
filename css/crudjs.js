// JavaScript Document
$(document).ready(function(){
		
	$('.thumbnail').mouseenter(function(){
		$(this).css('background','#CACACA');
	}).mouseleave(function(){
		$(this).css('background','white');
	});
	
	$('.dashmenu').mouseenter(function(){
		$(this).css('background','#4D4D4D');
	}).mouseleave(function(){
		$(this).css('background','#373737');
	});
	
			
});